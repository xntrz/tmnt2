        break;
        
    case STAGEID::ID_ST42NB:
    case STAGEID::ID_ST60X_B10:
        enemyId = ENEMYID::ID_LEATHER_HEAD;
        break;
        
    case STAGEID::ID_ST44NB:
    case STAGEID::ID_ST60X_D05:
        enemyId = ENEMYID::ID_ULTIMATE_NINJA;
        break;
        
    case STAGEID::ID_ST47OB:
    case STAGEID::ID_ST60X_C08:
        enemyId = ENEMYID::ID_FOOT_MECH_SPLINTER;
        break;
        
    case STAGEID::ID_ST52FB:
    case STAGEID::ID_ST60X_C07:
        enemyId = ENEMYID::ID_ELITE_FOOT_A;
        break;
        
    case STAGEID::ID_ST53FB:
        enemyId = ENEMYID::ID_ELITE_FOOT_B;
        break;

    case STAGEID::ID_ST54FB:
    case STAGEID::ID_ST60X_C04:
        enemyId = ENEMYID::ID_ELITE_FOOT_C;
        break;
        
    case STAGEID::ID_ST55FB:
    case STAGEID::ID_ST60X_C02:
