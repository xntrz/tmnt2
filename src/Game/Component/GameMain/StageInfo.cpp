/*static*/ bool CStageInfo::IsBossGaugeNecessary(STAGEID::VALUE idStage)
{
    ENEMYID::VALUE enemyId = GetGaugeEnemyID(idStage, 0);
    return (enemyId != ENEMYID::ID_DUMMY);
};


